char *inet_ntoa(struct in_addr ina)
{
	static char buf[4*sizeof "123"];
	unsigned char *ucp = (unsigned char *)&ina;

	diag_sprintf(buf, "%d.%d.%d.%d",
                     ucp[0] & 0xff,
                     ucp[1] & 0xff,
                     ucp[2] & 0xff,
                     ucp[3] & 0xff);
	return buf;
}

